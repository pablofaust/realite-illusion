    <div class="entree">

      <p class="date date-passee">19.10.17</p>

      <p class="carnet-de-bord">

        Je viens de revoir avec une amie <strong><a href="https://www.dpstream.net/serie-4624-black-mirror/saison-3/episode-4.html" target="_blank">l'épisode 4 de la troisième saison de <i>Black Mirror</i></a></strong>, où il est laissé le choix aux individus de rester dans un paradis virtuel et nostalgique s'ils ne veulent pas mourir. Évidemment, à la fin, elle m'a demandé ce que je choisirais. J'ai répondu que là où les mecs sont très forts, c'est qu'ils proposent "une version d'essai", exactement comme pour n'importe quel logiciel, et que c'est sûrement ça qui fait la différence : si on veut refuser, il faut refuser par principe, mais si on se laisse tenter par la version d'essai il y a de bonnes chances pour qu'on veuille la prolonger indéfiniment. 

        <br><br>

        Et là elle a dit quelque chose de très intéressant, quelque chose comme : "<i>Il y a un truc bizarre dans cette série, <strong>j'ai l'impression qu'ils partent du principe, dans chaque épisode, que l'être humain n'existe que par son esprit</strong></i>". Je n'ai pas tout de suite compris ce qu'elle voulait dire. Mais c'est vrai que l'épisode se termine sur une vision de tous ces esprits immortels qui palpitent dans des mini-puces fixées à des murs de serveurs informatiques. Ce qui suppose une copie informatique du contenu du cerveau, mais aussi du transfert, on ne sait trop comment, de ce qui correspond à la conscience. Je lui ai dit qu'il était plus concevable d'imaginer un cerveau qu'on continuerait d'alimenter à l'infini et qu'on ferait vivre dans une réalité augmentée rendue absolument crédible grâce à des informations envoyées dans le cerveau ou dans les nerfs (je me suis bien rendu compte en parlant que je n'y connais rien), un peu comme les drogues qui font apparaître des hallucinations et des sensations en créant des connexions involontaires entre les synapses. Elle ne pense pas qu'on arrive un jour à abolir la frontière. <strong>Elle n'arrêtait pas de répéter que ce ne serait pas la (vraie) vie</strong>. Qu'on a la preuve qu'on existe parce que les autres sont là pour témoigner de notre existence, que si elle me demande si elle existe je suis en mesure de lui répondre oui. J'ai répondu que si j'étais le développeur d'une intelligence artificielle, le premier cas d'erreur que je penserais à traiter serait justement ce moment où la personne en immersion demande à l'intelligence de lui prouver son existence, et que je lui ferait même montrer qu'elle est capable de se questionner sur la définition même d'existence, histoire d'être encore plus convaincante. 

        <br><br>

        <strong>On s'est aussi demandé si les images offrent une illusion de plus en plus convaincante</strong>, et si la frontière avec le réel est effectivement de plus en plus poreuse. Elle est convaincue que la confusion était la même pour des gens qui ne connaissaient que la peinture en perspective, parce qu'il n'ont connu que ça, que pour des gens qui peuvent s'immerger dans l'univers d'une série pendant plusieurs dizaines d'heures. Ce qui signifierait pour elle que <strong>l'être humain sera toujours capable de faire la distinction, quelles que soient les avancées techniques</strong>. Je ne peux pas m'empêcher de penser à l'inverse que la frontière est de plus en plus mince, et que chaque nouveau progrès dans l'image induit un nouveau seuil dans la perte de repères. 

        <br><br>

        Elle n'arrêtait pas de répéter que ce ne serait jamais possible parce qu'on aurait toujours besoin d'aller aux toilettes et de manger, même après que je lui aie dit qu'on pouvait complètement imaginer qu'on vive sous perfusion avec une poche à caca pendant que notre cerveau ganbaderait dans un paradis artificiel, un peu comme dans <i>Matrix</i>. Elle m'a dit qu'elle n'avait jamais fini ce film parce qu'elle l'avait trouvé trop con. Et elle a fini par demander à ce qu'on change de sujet, parce que ça la déprimait : <strong>la perspective de vivre dans une réalité virtuelle ne peut signifier pour elle qu'un appauvrissement</strong>, et même pire, une forme honteuse de vie, une insulte, voire la négation de la vie, enfin rien de souhaitable quoi.

        <br><br>

        Elle m'a aussi parlé d'un film qu'il va falloir que je voie : <i>Le monde sur le fil</i>, de Fassbinder.

      </p>

    </div>

    <div class="entree">

      <p class="date">20.10.17</p>

      <p class="carnet-de-bord">

        Du coup, ce soir on a regardé la première partie du <a href="https://youtu.be/jfXtzBRX3sg" target="_blank"><strong><i>Monde sur le fil (Welt am Draht)</i> de Fassbinder (1973)</strong></a>, adapté d'un roman de Daniel F. Galouye, <i>Simulacron 3</i> (1969) - au passage, je découvre qu'il se rattache à <strong><a href="https://fr.wikipedia.org/wiki/Simulacre_(science-fiction)" target="_blank">la science-fiction des <i>simulacres</i></a></strong>, qui désigne précisément des scénarios où l'homme tente de simuler la réalité (la société ou l'humain), et à laquelle se rattachent de nombreux <strong>romans de Philip K. Dick</strong>, mais aussi, évidemment, <i>Matrix</i>. Sans parler de la filiation directe avec <strong><i>Westworld</i></strong>.

        <br><br>

        Dans la première partie du film que j'ai vue, des scientifiques recréent des "unités" humaines grâce à des ordinateurs, donnant ainsi naissance à un simulacre de société algorithmée. Pour l'instant, deux menaces principales : l'intérêt de l'industrie pour le potentiel d'anticipation d'une telle matrice, et le risque de confusion, pour les scientifiques, entre la réalité et sa simulation ("<strong><i>Réfléchis : chaque jour, tu règnes comme Dieu sur un monde miniature que tu as contribué à créer, et que tu confonds de plus en plus avec le monde réel</i>", <a href="https://youtu.be/jfXtzBRX3sg?t=1h10m42s" target="_blank">1:10:45</a></strong>) - je viens juste de me rendre compte à quel point c'est intéressant de noter qu'<strong>on peut dire aussi bien <i>vrai monde</i> que <i>monde réel</i>, comme si la vérité se trouvait forcément dans la réalité, pas dans l'illusion</strong>.

        <br><br>

        <img src="zenon.jpg" class="image-entouree-droite" alt="Schéma du paradoxe d'Achille et la tortue">

        Il y est aussi question d'<strong>un des paradoxes de Zénon : <a href="https://fr.wikipedia.org/wiki/Paradoxe_d%27Achille_et_de_la_tortue" target="_blank">le paradoxe d'Achille et la tortue</a></strong>. Ça me disait vaguement quelque chose ; je me le suis fait réexpliquer : Achille aura beau être plus rapide que la tortue, s'il la laisse partir avec de l'avance, il ne la rattrapera jamais, parce qu'à chaque fois qu'il parcourera la distance qui le sépare de la tortue celle-ci aura progressé, créant une nouvelle distance... et ainsi de suite à l'infini. Je ne voyais pas le lien avec la réalité virtuelle, mais mon amie, non seulement plus cultivée mais aussi plus vive que moi, l'avais trouvé : il est question de savoir si deux réalités différentes peuvent se rejoindre, ou plutôt si celle qui a du retard sur l'autre (la simulation) peut rattraper la première (ses créateurs), ce qui n'est pas sans évoquer le concept de <i><strong><a href="https://fr.wikipedia.org/wiki/Singularit%C3%A9_technologique" target="_blank">singularité</a>, inventé par Raymond Kurzweil</strong></i>, qui est <strong>ce fameux point de rupture hypothétique où l'intelligence artificielle dépassera l'intelligence humaine</strong> et où les choses risquent de s'emballer à la vitesse de la puissance de calcul des machines... Ce qu'on retrouve dans <i>Westworld</i>, où <strong>la singularité s'accompagne d'un éveil des robots à la conscience</strong>. Tellement de choses qu'il me faut approfondir !

      </p>

