    <h1>Réalité | Illusion</h1>

    <div class="entree">

      <p class="date date-passee">17.10.17</p>

      <p class="carnet-de-bord">

        J’ai souvent l’impression que, quand on le regarde longtemps et de très haut, <strong>le monde humain est construit sur des couples de forces opposées</strong>, présentes en chaque élément comme autant de dosages minutieux et changeants. Par exemple : inné | acquis. Le premier qui l’a évoqué devant moi était un prof qui affirmait que le monde néolibéral repose sur la croyance enfouie que tout est inné. Depuis, j’ai cru sentir, d’abord chez mes amis, que chacun d’entre nous est plus ou moins biaisé en faveur de l’inné ou de l’acquis. On est tous prêts à reconnaître qu’il y a une certaine part d’inné et une certaine part d’acquis chez l’être humain. Pour certain ce sera 50|50, pour d’autre 70|30. Je pense que quelques rares personnes sont même prêtes à réduire la part de l’inné à presque rien, tandis que l’acquis est presque toujours reconnu. Par contre, l’inné a pour lui ce caractère péremptoire, inflexible, c’est lui qui dit : « si je suis comme ça je n’y peux rien », et il arrive que très peu d’inné l’emporte sur beaucoup d’acquis. On peut d’ailleurs constater que l’inné est globalement mieux partagé : c’est à la fois une idée très vieille et très à la mode - qui est à l’animal. C’est aussi une idée plus simple, plus poétique. Pour autant, le plus souvent, les gens ne sauraient pas quantifier. Précisément parce que le rapport de force est inquantifiable, que la répartition est indémontrable, le débat n’est jamais clos, et rarement explicite. Pourtant, il y a beaucoup de réactions, dans beaucoup d’aspects de la vie, qu’on sent naître de ce fameux biais en faveur de l’un ou de l’autre des deux contraires : face à la responsabilité (naturelle | sociale), face à l’amour (coup de foudre | construction volontaire), face aux choix politiques, etc. 

        <br><br>

        Mais ce n’est pas cette opposition que je veux creuser. Ça doit faire bien trois ans que j’y réfléchis par petites touches : j’en suis venu à penser qu’on ne tranchera jamais, et que d'ailleurs cette ligne de partage sépare grosso modo la gauche de la droite : l’acquis, le social et les excuses à gauche | l’inné, la volonté et la compétition à droite. Je sais que ce bilan est facile, expéditif et probablement erroné, mais pour l’instant ça me va. Je n’ai pas envie pour l’instant de pousser plus loin cette question insoluble – même si <strong>c’est précisément son insolvabilité qui fait d’elle une force motrice</strong>. Je ne la trouve pas urgente, et même si elle est actuelle, elle ne fait pas l’actualité.

        <br><br>

        Une autre opposition qui me travaille inconsciemment, depuis longtemps, depuis que je distinguais <strong>nature | culture</strong> dans mes recherches d’arts plastiques au lycée, et qui se pose à moi de façon plus urgente ces derniers temps, c’est <strong>l’opposition réalité | illusion</strong>. J’ai l’impression que cette ligne de partage-ci remonte très loin dans l’Histoire. L’intuition qu’on la trouve <strong>déjà dans les textes bouddhistes</strong>, sans avoir encore pris le temps de les lire. J’imagine, par de vagues réminiscences scolaires, qu’elle est <strong>présente chez les Grecs</strong>. Je me souviens d’<strong>un débat sur le danger de la fiction à l’époque de <i>Rocambole</i></strong> – débat presque éteint aujourd’hui, qui mériterait d’être rallumé au contact des séries. Je crois savoir que <strong>Proust a posé une pierre à l’édifice avec son œuvre</strong>, plutôt en faveur de l’illusion, d’après ce que j’ai cru comprendre. Je peux aussi penser à <strong><i>Matrix</i></strong>, au choix de la pilule bleue ou de la pilule rouge. À <strong>Guy Debord, évidemment, et sa vie de lutte contre la <i>Société du Spectacle</i></strong>.

        <br><br>

        <strong>Au sein de toutes ces philosophies et de toutes ces œuvres, je peux voir courir la faille réalité | illusion. Et surtout, j’en pressens l’urgence (il ne faut pas être devin) à l’aube de la réalité virtuelle et des vies algorithmées.</strong>

        <br><br>

        Je me propose donc de faire de ces deux oppositions un filet, réalité | illusion, et de le tendre au vent jusqu’en juin 2018. On verra bien ce que je pêche.

      </p>

    </div>

    <div class="entree">

      <p class="date date-passee">18.10.17</p>

      <p class="carnet-de-bord">

        <strong>La question qui se pose : l'un ou l'autre, réalité ou illusion, a-t-il plus de valeur ou plus de poids ?</strong> Faut-il donner la priorité à l'un des deux ? Faut-il fuir la fuite de la réalité contenue dans l'illusion ? Au contraire, l'illusion est-elle le propre de la vie humaine ? Y a-t-il même une différence entre réalité et illusion ?

        <br><br>

        Au passage, d'autres sources me sont venues pour enrichir le corpus : <strong>les <i>Illusions perdues</i> de Balzac</strong>, qui déjà dénonce la fumée publicitaire. <strong>Les réalités imaginaires de Harari, dans <i>Sapiens</i></strong>, formes primitives et essentielles de la collaboration humaine. <strong>Certains épisodes de <i>Black Mirror</i>, notamment "15 millions de mérites", "Be Right Back", "Playtest" et "San Jupitero"</strong>, qui présentent tous des dystopies avec une part d'illusion.

        <br><br>

        En voulant commencer à jeter spontanément sur le "papier" - de mon logiciel de graphisme - le début d'une carte conceptuelle, je me rends compte que j'écris "réalité" à gauche, "illusion" à droite, et que je n'envisage pas de faire l'inverse. Je crois même que c'est ce que j'ai fait en première intention, mais ça n'allait pas, et j'ai échangé. Je me souviens d'un vieux manuel de PNL (programmation neuro-linguistique, en général résumé - surtout par les dragueurs de base - à la façon dont votre corps vous trahit) où il était écrit que regarder à gauche signifierait penser au passé, tandis que regarder à droite signifierait se projeter vers le futur...

      </p>

